    public Loading(MainFrame ss) {
        
       initComponents();
        setVisible(true);
         ss=new MainFrame();
         
        try{
            
        for(int i=0;i<100;i++)
        {
            Task s=new Task(this,ss);
            s.Start();
            if(ss.getFinish()==true)
                s.Stop();
       Thread.sleep(100);
        this.ProgressLabel.setText(Integer.toString(i)+"%");
        this.jProgressBar1.setValue(i);
        if(i==99)
        {
            dispose();
         
     ss.setVisible(true);
        }
        }
        }catch(Exception e) {
        System.err.println("Error"+e.getMessage());
        }
    }

